    private void checkConn() {
        try {
            // Check availability of network connection.
            ConnectivityManager connMgr = (ConnectivityManager) getSystemService(Context.CONNECTIVITY_SERVICE);
            NetworkInfo networkInfo = connMgr.getActiveNetworkInfo();
            Boolean isConnected = networkInfo != null && networkInfo.isConnectedOrConnecting();
            if (isConnected) {
                attemptLogin();
            } else {
                Toast.makeText(getApplication(), "Network is NOT available",
                        Toast.LENGTH_LONG).show();
            }
        } catch (Exception e) {
            Toast.makeText(getApplication(),
                    "Error reading record:" + e.getMessage(),
                    Toast.LENGTH_LONG).show();
        }
    }

    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private final String mEmail;
        private final String mPassword;
        private Context c;
        private boolean valid = false;
        private Student studentL;

        UserLoginTask(String email, String password, Context c) {
            mEmail = email;
            mPassword = password;
            this.c = c;
        }

        @Override
        protected Boolean doInBackground(Void... params) {
            // TODO: attempt authentication against a network service.

            String url = getApplicationContext().getString(R.string.validate_student);
            final Student student = new Student();
            student.setEmail(mEmail);
            student.setPassword(mPassword);
            RequestQueue queue = Volley.newRequestQueue(c);
            //Send data
            try {
                StringRequest postRequest = new StringRequest(
                        Request.Method.POST,
                        url,
                        new Response.Listener<String>() {
                            @Override
                            public void onResponse(String response) {
                                try{
                                    JSONArray obj = new JSONArray(response);
                                    for(int index = 0;index < obj.length();index++){
                                        if(obj.getJSONObject(index) != null){
                                            JSONObject courseResponse = obj.getJSONObject(index);
                                            String studId = courseResponse.getString("studId");
                                            String name = courseResponse.getString("name");
                                            String password = courseResponse.getString("password");
                                            String email = courseResponse.getString("email");
                                            String telNo = courseResponse.getString("telno");
                                            Student student = new Student();
                                            student.setStudId(studId);
                                            student.setName(name);
                                            student.setPassword(password);
                                            student.setEmail(email);
                                            student.setTelNo(telNo);
                                            studentL = student;
                                            valid = true;
                                        }
                                    }

                                }catch (Exception e){
                                    Toast.makeText(getApplicationContext(), "Error:" + e.getMessage(), Toast.LENGTH_LONG).show();
                                }

                            }
                        },
                        new Response.ErrorListener() {
                            @Override
                            public void onErrorResponse(VolleyError error) {
                                Toast.makeText(getApplicationContext(), "Error. " + error.toString(), Toast.LENGTH_LONG).show();
                            }
                        }){
                    @Override
                    protected Map<String, String> getParams() {
                        Map<String, String> params = new HashMap<>();
                        params.put("email", student.getEmail());
                        params.put("password", student.getPassword());
                        return params;
                    }

                    @Override
                    public Map<String, String> getHeaders() throws AuthFailureError {
                        Map<String, String> params = new HashMap<>();
                        params.put("Content-Type", "application/x-www-form-urlencoded");
                        return params;
                    }
                };
                queue.add(postRequest);
            } catch (Exception e) {
                e.printStackTrace();
            }

            //showProgress(true);

            try {
                // Simulate network access.
                Thread.sleep(2000);
            } catch (InterruptedException e) {
                return false;
            }


            // TODO: register the new account here.
            return valid;
        }

        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(false);


            if (success) {
                Toast.makeText(LoginActivity.this, "Successful login", Toast.LENGTH_SHORT).show();
                SessionManager sess = new SessionManager(c);
                sess.createLoginSession(mEmail, mPassword, studentL.getName(), studentL.getStudId());
                Intent intent = new Intent(c, HomescreenActivity.class);
                pDialog.dismiss();
                startActivity(intent);

            } else {

                mEmailView.setError("");
                mPasswordView.setError(getString(R.string.error_incorrect_password));
                mPasswordView.requestFocus();
                pDialog.dismiss();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
